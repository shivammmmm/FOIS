export function normalizeCommodityCode(code) {
  return String(code || "").trim().toUpperCase().replace(/[\s._-]+/g, "");
}

export function getCommodityDisplayName(code) {
  if (!code) return "-";
  const normalized = normalizeCommodityCode(code);
  return COMMODITY_DICTIONARY[normalized] || String(code);
}
